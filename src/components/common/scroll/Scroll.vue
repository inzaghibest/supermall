<template>
<!-- ref/children -->
  <div class="wrapper" ref="wrapper">
      <div class="conntent">
          <slot></slot>
      </div>
  </div>
</template>

<script>
import BScroll from 'better-scroll'
export default {
    name: 'Scroll',
    data () {
        return {
            scroll: null
        }
    },
    props: {
        // 由父组件决定是否实时监听
        probeType: {
            type: Number,
            default: 0
        }
    },
    mounted () {
        this.scroll = new BScroll(this.$refs.wrapper, {
            click: true,
            probeType: this.probeType
        })
    },
    methods: {
        scrollTo(x, y, time = 500) {
            this.scroll.scrollTo(x, y, time)
        }
    }
}
</script>

